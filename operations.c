#include <stdio.h>
#include <stdlib.h>

int soma(int numero1, int numero2) {
	return numero1 + numero2;
}

int subtracao(int numero1, int numero2) {
	return numero1 - numero2;
}

int multiplicacao(int numero1, int numero2) {
	return numero1 * numero2;
}

int divisao(int numero1, int numero2) {
	return numero1 / numero2;
}